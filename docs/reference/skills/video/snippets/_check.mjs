// Snippet validator for /video skill.
// Validates structure + cross-references of every <name>.md under
// docs/reference/skills/video/snippets/.
//
// Usage: node _check.mjs            -> walk all snippets
//        node _check.mjs --file x.md -> one file
//
// Rules:
//  1. Frontmatter parses as YAML, has required keys: name, purpose, tags.
//  2. Body contains exactly one h1 (^# <title>).
//  3. All internal links (./<other>.md) resolve to existing files in
//     the same dir.
//  4. All rule_id references (tokens matching /\b(comp|nf|linter)\/[a-z0-9-]+/)
//     exist in the linter's rule registry (parsed from the canonical
//     composition-linter/rules/*.js files).
//  5. Length warning if > 130 lines (does not fail).

import { readFile, readdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join, basename } from 'node:path';
import { existsSync } from 'node:fs';

const __dirname = dirname(fileURLToPath(import.meta.url));

// -- Step 1: parse front-matter -------------------------------------------
function parseFrontmatter(text) {
  if (!text.startsWith('---\n')) return { frontmatter: null, body: text };
  const end = text.indexOf('\n---\n', 4);
  if (end === -1) return { frontmatter: null, body: text };
  const header = text.slice(4, end);
  const body = text.slice(end + 5);
  const fm = {};
  const lines = header.split('\n');
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    const m = line.match(/^([a-zA-Z_][\w-]*):\s*(.*)$/);
    if (!m) { i++; continue; }
    let [_, k, v] = m;
    v = v.trim();

    if (v.startsWith('[') && v.endsWith(']')) {
      // Flow-style array
      fm[k] = v.slice(1, -1).split(',').map(s => s.trim().replace(/^["']|["']$/g, '')).filter(Boolean);
      i++;
    } else if (v === '') {
      // Might be a block-style array; collect `  - …` lines.
      const arr = [];
      let j = i + 1;
      while (j < lines.length && /^\s{2,}-\s+/.test(lines[j])) {
        arr.push(lines[j].replace(/^\s*-\s+/, '').trim().replace(/^["']|["']$/g, ''));
        j++;
      }
      if (arr.length > 0) {
        fm[k] = arr;
        i = j;
      } else {
        fm[k] = '';
        i++;
      }
    } else {
      fm[k] = v.replace(/^["']|["']$/g, '');
      i++;
    }
  }
  return { frontmatter: fm, body };
}

// -- Step 2: load rule registry from the linter source -------------------
async function loadRuleIds() {
  const rulesDir = join(
    __dirname, '..', '..', '..', '..', '..',
    'src/nevoflux/extensions/nevoflux-agent/lib/composition-linter/rules'
  );
  const files = await readdir(rulesDir);
  const ids = new Set();
  for (const f of files) {
    if (!f.endsWith('.js')) continue;
    const text = await readFile(join(rulesDir, f), 'utf8');
    for (const m of text.matchAll(/rule_id:\s*['"]((?:comp|nf|linter)\/[a-z0-9-]+)['"]/g)) {
      ids.add(m[1]);
    }
  }
  return ids;
}

// -- Step 3: per-snippet check --------------------------------------------
async function checkSnippet(path, ruleIds) {
  const text = await readFile(path, 'utf8');
  const problems = [];
  const warns = [];

  const { frontmatter, body } = parseFrontmatter(text);
  if (!frontmatter) {
    problems.push('no YAML frontmatter found');
  } else {
    for (const key of ['name', 'purpose', 'tags']) {
      if (!frontmatter[key]) problems.push(`frontmatter missing "${key}"`);
    }
  }

  const h1Count = (body.match(/^# /gm) || []).length;
  if (h1Count !== 1) problems.push(`expected exactly 1 h1, found ${h1Count}`);

  const here = dirname(path);
  for (const link of body.matchAll(/\]\(\.\/([\w.-]+\.md)\)/g)) {
    if (!existsSync(join(here, link[1]))) {
      problems.push(`broken internal link: ./${link[1]}`);
    }
  }

  for (const m of body.matchAll(/\b((?:comp|nf|linter)\/[a-z0-9-]+)\b/g)) {
    // linter/* are synthetic rule_ids generated by the linter itself, so they're always valid
    const isSynthetic = m[1].startsWith('linter/');
    if (!isSynthetic && !ruleIds.has(m[1])) {
      problems.push(`unknown rule_id reference: ${m[1]}`);
    }
  }

  const lines = text.split('\n').length;
  if (lines > 130) warns.push(`length ${lines} > 130 (soft limit)`);

  return { problems, warns };
}

// -- Step 4: main ---------------------------------------------------------
const args = process.argv.slice(2);
const fileIdx = args.indexOf('--file');
const targets = fileIdx > -1
  ? [args[fileIdx + 1]]
  : (await readdir(__dirname))
      .filter(f => f.endsWith('.md'))
      .map(f => join(__dirname, f));

const ruleIds = await loadRuleIds();
let failCount = 0;
let warnCount = 0;
for (const t of targets) {
  const { problems, warns } = await checkSnippet(t, ruleIds);
  if (problems.length === 0 && warns.length === 0) {
    console.log(`PASS ${basename(t)}`);
  } else {
    if (problems.length) {
      console.error(`FAIL ${basename(t)}`);
      for (const p of problems) console.error(`  - ${p}`);
      failCount++;
    }
    if (warns.length) {
      console.warn(`WARN ${basename(t)}`);
      for (const w of warns) console.warn(`  - ${w}`);
      warnCount++;
    }
  }
}
console.log(`\n${targets.length - failCount} pass, ${failCount} fail, ${warnCount} warn`);
process.exit(failCount > 0 ? 1 : 0);
